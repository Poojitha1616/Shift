public class Shift {  
    public static void main( String[ ] args ) {  
        int x = 5 ;    
        int y = x << 4 ;  
        int z = x >> 4 ; 
        System.out.println( " The left shifted value : " +y) ;
        System.out.print( " The right shifted value is : " +z) ;    
    }  
} 
